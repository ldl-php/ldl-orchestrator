<?php

declare(strict_types=1);

namespace LDL\Example\Build\Application\User;

class Application
{
    /**
     * @var string
     */
    private $url;

    public function __construct(string $url)
    {
        $this->url = $url;
    }

    public function getUrl(): string
    {
        return $this->url;
    }
}
